public class Horward {
    String name;
    int power;
    int distance;

    public Horward(String name,int power, int distance) {
        this.name = name;
        this.power = power;
        this.distance = distance;
    }

    public String getName() {
        return name;
    }

    public int getPower() {
        return power;
    }

    public int getDistance() {
        return distance;
    }
}
